package CalculatorAPI.CalculatorAPI;

public class CalculatorAPI {

	public  int addition(int num1, int num2) {
		int res;
		res = num1 + num2;
		return res;
	}
	public  int subtraction(int num1, int num2) {
		int res;
		res = num1 - num2;
		return res;
	}
	
	public  int multiplicatio(int num1, int num2) {
		int res;
		res = num1 * num2;
		return res;
	}
	
}
